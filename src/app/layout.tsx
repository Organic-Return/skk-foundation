export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  // Fetch settings and navigation
  const [settings, branding, mainNav, footerNav] = await Promise.all([
    getSettings(),
    getBranding(),
    getMainNavigation(),
    getFooterNavigation(),
  ]);

  const footerColumns = groupFooterLinks(footerNav);
  const template = settings?.template || 'classic';

  // Render appropriate header based on template
  const renderHeader = () => {
    if (template === 'luxury') {
      return (
        <LuxuryHeader
          logo={branding?.logo}
          logoAlt={branding?.logoAlt || settings?.title}
          siteTitle={settings?.title}
          navItems={mainNav}
          phoneNumber={settings?.contactInfo?.phone}
          email={settings?.contactInfo?.email}
        />
      );
    }
    if (template === 'modern') {
      return (
        <ModernHeader
          logo={branding?.logo}
          logoAlt={branding?.logoAlt || settings?.title}
          siteTitle={settings?.title}
          navItems={mainNav}
          phoneNumber={settings?.contactInfo?.phone}
          email={settings?.contactInfo?.email}
        />
      );
    }
    return (
      <Header
        logo={branding?.logo}
        logoAlt={branding?.logoAlt || settings?.title}
        siteTitle={settings?.title}
        navItems={mainNav}
      />
    );
  };

  // Determine template-specific body class
  const templateClass = template === 'modern' ? 'modern-template' : '';

  return (
    <html lang="en">
      <body className={`${inter.variable} ${lora.variable} ${cormorantGaramond.variable} ${montserrat.variable} antialiased ${templateClass}`}>
        <AuthProvider>
        <LayoutWrapper
          header={renderHeader()}
          footer={
            <Footer
              logo={branding?.logo}
              logoAlt={branding?.logoAlt || settings?.title}
              siteTitle={settings?.title}
              description={settings?.description}
              columns={footerColumns}
              socialMedia={settings?.socialMedia}
              contactInfo={settings?.contactInfo}
              footer={settings?.footer}
            />
          }
        >
          {children}
        </LayoutWrapper>
        </AuthProvider>
      </body>
    </html>
  );
}
